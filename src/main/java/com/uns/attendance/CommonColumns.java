package com.uns.attendance;

public class CommonColumns {
	
	private String teacher;
	
	private String topics;
	
	private String homeworkGiven;
	
	private String activitiesPerformed;
	
	private String plansForNextday;

	public String getTeacher() {
		return teacher;
	}

	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}

	public String getTopics() {
		return topics;
	}

	public void setTopics(String topics) {
		this.topics = topics;
	}

	public String getHomeworkGiven() {
		return homeworkGiven;
	}

	public void setHomeworkGiven(String homeworkGiven) {
		this.homeworkGiven = homeworkGiven;
	}

	public String getActivitiesPerformed() {
		return activitiesPerformed;
	}

	public void setActivitiesPerformed(String activitiesPerformed) {
		this.activitiesPerformed = activitiesPerformed;
	}

	public String getPlansForNextday() {
		return plansForNextday;
	}

	public void setPlansForNextday(String plansForNextday) {
		this.plansForNextday = plansForNextday;
	}
	
}
